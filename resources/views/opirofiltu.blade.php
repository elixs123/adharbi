      <h1>O PIROFILITU</h1>
    </div>
    <p>Pirofilitni škriljac spada u grupu alumosilikatnih minerala hemijske formule Al2O34SiO2xH2O. Ime je dobio od grčke riječi pir-vatra i phillon-list, jer se pri žarenju lepezasto razlistava. Pirofilit ima troslojnu kristalnu rešetku koja u vanjskom sloju ima tetraedarske, a u unutrašnjem oktaedarske strukture. Kristali su pločasti, mekani i masni na opip. Elektroneutralan je i nije reaktivan u prirodnom obliku. Pirofilit nije rastvorljiv u vodi i ne bubri.
    U dosadašnjim istraživanjima u pirofilitnom škriljcu je potvrđeno prisustvo 34 minerala, te preko 60 hemijskih elemenata. Pirofilit se u prirodi javlja u formi škriljca. U ovisnosti od primjesa, boja pirofilita varira od bjele, sive do ljubičaste. 
    Usitnjavanjem pirofilitnog škriljca i mehanohemijskom aktivacijom mijenja se struktura alumosilikata. Razbijaju se veze alumosilikata i OH grupa, povećava se specifična površina i poroznost čestica, smanjuje veličina čestica, povećava rastvorlјivost i kapacitet katijonske izmjene, što sve doprinosi njegovoj većoj reaktivnosti i naelektrisanju površine. Zahvaljujući ovim osobinama, pirofilit ima jako širok spektar upotebe.
    </p>
    <p>Reference naučnih radova u kojima su dokazane osobine pirofilitnog škriljca dostupne su u Arhivi INRC-a.</p>
